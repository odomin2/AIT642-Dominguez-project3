package edu.towson.cis.cosc603.project4.rectangle;

/**
 * The Point Class.
 */
public class Point {
	
	/** x and y coordinates. */
	public Double length, width;
	
	/**
	 * Instantiates a new point.
	 *
	 * @param x the length
	 * @param y the width
	 */
	Point(Double x, Double y) {
		this.length = x;
		this.width = y;
	}
}
